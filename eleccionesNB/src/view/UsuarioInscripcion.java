    private void botonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConsultarActionPerformed
        int numeroCandidato = Integer.parseInt(textoNumeroCandidato.getText());
        CandidatoDTO candidatoDTO = new CandidatoDTO(0, 0, numeroCandidato, null);
        
        List lista = new ArrayList();
        
        try {
            lista = candidatoDTO.consultarCandidato();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UsuarioInscripcion.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if (lista.isEmpty()) {
            // vista no se encuentra candidato
            JOptionPane.showMessageDialog(null, "No se encuentra el candidato");
        } else {
            // vista impresion informacion
            JOptionPane.showMessageDialog(null, lista);
        }
    }//GEN-LAST:event_botonConsultarActionPerformed

