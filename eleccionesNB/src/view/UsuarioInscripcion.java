    public void crearInscripcion() throws ClassNotFoundException {
        int cedula = Integer.parseInt(textoCedula.getText());
        int idProceso = Integer.parseInt(textoIdProceso.getText());
        int numeroCandidato = Integer.parseInt(textoNumeroCandidato.getText());
        java.sql.Timestamp fechaInscripcion = java.sql.Timestamp.valueOf(textoFechaInscripcion.getText());

        CandidatoDTO candidatoDTO = new CandidatoDTO(cedula, idProceso, numeroCandidato, fechaInscripcion);

        String mensaje;
        mensaje = candidatoDTO.crearCandidato();
        JOptionPane.showMessageDialog(null, mensaje);
    }

